#include <stdio.h>

void main(){

    int num = 4;
    
    //soma
    int soma = num+num;
    // Multiplicação
    int mult = num*num;
    // Subtração
    int subt = num-num;
    // Divisão
    int divi = num/num;
    // Resto
    int rest = num%num;

    printf("Soma: %i\n", soma);
    printf("Multiplicacao: %i\n", mult);
    printf("Subtracao: %i\n", subt);
    printf("Divisao: %i\n", divi);
    printf("Resto: %i\n", rest);

}